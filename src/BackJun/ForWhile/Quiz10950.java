package BackJun.ForWhile;

import java.util.Scanner;

public class Quiz10950 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		
		for (int i = 0 ; i <a ;i++) {
			int i1 = sc.nextInt();
			int i2 = sc.nextInt();
			System.out.println(i1+i2);
		}
		
	}

}
